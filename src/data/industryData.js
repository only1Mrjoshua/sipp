// Get all industries for dropdown
export const getAllIndustries = () => {
  return Object.keys(industryData).sort();
};

// Get matching departments for an industry
export const getMatchingDepartmentsForIndustry = (industry) => {
  return industryData[industry]?.matchingDepartments || [];
};

// Get internship types for a specific industry
export const getInternshipTypesForIndustry = (industry) => {
  return industryData[industry]?.internshipTypes || [];
};

// Get skills required for a specific industry
export const getSkillsRequiredForIndustry = (industry) => {
  return industryData[industry]?.skillsRequired || [];
};

// Get skills offered for a specific industry
export const getSkillsOfferedForIndustry = (industry) => {
  return industryData[industry]?.skillsOffered || [];
};

// Get benefits for a specific industry
export const getBenefitsForIndustry = (industry) => {
  return industryData[industry]?.benefits || [];
};

// Find matching industries for a student's department
export const getMatchingIndustriesForDepartment = (department) => {
  const matches = [];
  for (const [industry, data] of Object.entries(industryData)) {
    if (data.matchingDepartments.includes(department)) {
      matches.push(industry);
    }
  }
  return matches;
};

// Get all internship types across all industries (for search)
export const getAllInternshipTypes = () => {
  const allTypes = new Set();
  for (const data of Object.values(industryData)) {
    data.internshipTypes.forEach(type => allTypes.add(type));
  }
  return Array.from(allTypes).sort();
};

// Get all skills across all industries
export const getAllIndustrySkills = () => {
  const allSkills = new Set();
  for (const data of Object.values(industryData)) {
    if (data.skillsRequired) {
      data.skillsRequired.forEach(skill => allSkills.add(skill));
    }
  }
  return Array.from(allSkills).sort();
};